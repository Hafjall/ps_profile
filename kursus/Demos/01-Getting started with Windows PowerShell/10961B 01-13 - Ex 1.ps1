# For at få hjælp


# F.eks hjælp på "Dir" som faktisk er en Alias for "Get-ChildItem

Get-Help dir

# Derfor give denne kommando også samme resultat

Get-Help Get-ChildItem

# Man kan også få vist hjælp som en vindue...

Get-Help Get-ChildItem -ShowWindow 

# Man kan få eksempler på anvendelse

Get-Help Get-ChildItem -Examples

# Man kan forkorte "Get-Help" til aliasen "Help"

Help dir -Examples

# Man kan også gå direkte online hos MS og se den seneste opdaterede hjælp informantion

Help Get-ChildItem -Online


